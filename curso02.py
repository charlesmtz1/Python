#Curso de Python 3: Variables
#
#--La declaracion de variables se hace escribiendo el nombre en minusculas.
#--No se requiere ningun caracter especial o palabra reservada inicial.
#--Se recomienda usar nombres expresivos, no declarar variables como "x" o "y".
#--Para variables con palabra compuestas, se declaran con guion bajo para los espacios: mi_variable
#--Una variable no debe empezar con numero o caracter especial.
#
#Tipos de variables disponibles:
#Caracteres: "A"
#Numeros enteros: 2
#Numeros flotantes: 2.5
#Cadenas: "Hola Mundo"
#Booleanos: True/False

caracter = "A"
entero = 2
flotante = 2.5
cadena = "Hola Mundo"
boolean = False

print("Variable de caracter:", caracter)
print("Variable entera:", entero)
print("Variable flotante:", flotante)
print("Variable cadena:", cadena)
print("Variable booleana:", boolean)
